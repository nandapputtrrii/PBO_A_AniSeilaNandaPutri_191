package Model;

public class Member {
    public String nama, fakultas, prodi, no_hp, email, nim;

    public Member(String nama, String fakultas, String prodi, String no_hp, String email, String nim) {
        this.nama = nama;
        this.fakultas = fakultas;
        this.prodi = prodi;
        this.no_hp = no_hp;
        this.email = email;
        this.nim = nim;
    }
}